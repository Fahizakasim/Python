#FOR LOOP -- Printing the character of the string.
for name in "fahiza": 
    print(name)

#FOR LOOP -- Printing the numbers in range(begin, end+1, jump of 10):    
for name in range (0, 100, 10):
    print(name)

#FOR LOOP -- Printing the numbers in set in sorted order:    
for name in sorted({'1','2','3','4','5'}):
    print(name)

#FOR LOOP -- Printing the numbers in range(begin, end+1):    
for i in range(1,101):
    print(i)

 #FOR LOOP -- Printing the numbers in reverse order:   
for i in range(10,0,-1):
    print(i)

#FOR LOOP -- Nested for loop for i and j:

for i in range(1,4):
    for j in range(2,5):
        print(i,j)